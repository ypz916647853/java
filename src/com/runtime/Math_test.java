package com.runtime;

public class Math_test {

	public static void main(String[] args) {
		System.out.println("-5.5的绝对值为:"+Math.abs(-5.5));
		System.out.println("4的正弦为:"+Math.sin(4));
		System.out.println("4的余弦为:"+Math.cos(4));
		System.out.println("4的正切为:"+Math.tan(4));
		System.out.println("5的平方根为:"+Math.sqrt(5));
		System.out.println("36的立方根为:"+Math.cbrt(36));
		System.out.println("5的8次方为:"+Math.pow(5, 8));
		System.out.println("大于5.5的最小整数为:"+Math.ceil(5.5));
		System.out.println("小于5.5的最大整数为:"+Math.floor(5.5));
		System.out.println("对7.3进行四舍五入的结果为:"+Math.round(7.3));
		System.out.println("7.8和8.7大的那位数为:"+Math.max(7.8, 8.7));
		System.out.println("7.8和8.7小的那位数为:"+Math.min(7.8, 8.7));
		
	}

}
